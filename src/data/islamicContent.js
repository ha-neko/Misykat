export function getRandomContent(type) {
  if (type === 'hadith') return wakeUpHadith[Math.floor(Math.random() * wakeUpHadith.length)];
  if (type === 'surah') return wakeUpSurah[Math.floor(Math.random() * wakeUpSurah.length)];
  if (type === 'ceramah') return wakeUpCeramah[Math.floor(Math.random() * wakeUpCeramah.length)];
  const allContent = [...wakeUpHadith, ...wakeUpSurah, ...wakeUpCeramah];
  return allContent[Math.floor(Math.random() * allContent.length)];
}

export function getContentByType(type) {
  return getRandomContent(type);
}
